	def self.from_image_dataset_metadata_old (image, delete_after=true)

		metadata = image.metadata

		appropriate_keys = metadata.to_a.map(&:address) & self.attributes.keys.map{|item| item.gsub(/^_/,'').gsub(/_/,',')}

		choices = {}
		choices['image_dataset_id'] = image.id
		appropriate_keys.each do |key|
			underscore = "_#{key.gsub(",","_")}"
			first_meta = metadata.where(:address => key).first
			choices[underscore] = first_meta.nil? ? nil : first_meta.value.to_s
		end

		if delete_after
			metadata.each do |datum|
				datum.delete
			end
		end

		return self.new(choices)
	end

	def self.from_image_dataset_taghash (image)

		begin
			taghash = image.dicom_taghash
			if !taghash.nil?
				appropriate_keys = taghash.keys & self.attributes.keys.map{|item| item.gsub(/^_/,'').gsub(/_/,',')}

				choices = {}
				choices['image_dataset_id'] = image.id
				appropriate_keys.each do |key|
					underscore = "_#{key.gsub(",","_")}"
					if !taghash[key][:value].nil? and !taghash[key][:value].blank?
						choices[underscore] = "#{taghash[key][:value]}".encode("UTF-8", invalid: :replace, undef: :replace, replace: "")
					end
				end
			end
			return self.new(choices)
		rescue Psych::SyntaxError
			puts "image #{image.id} has weirdness (psych::syntax error)"
		end

	end

